package ch01;

public class copy {

	// 중괄호 (블록) 에 범위는 절대적이다.
	// main이란 : 프로그램의 시작 점
	public static void main(String[] args) {
		
		int age; // 변수 선언 int : 자료형 age : 변수
		int count;

		age = 1; // 초기화 한다 (값을 넣다.)
		count = 100; // 구문에 끝은 세미콜론으로 한다.

		// 화면에 age라는 변수에 담겨있는 값을 출력해줘!!!
		System.out.println(age);
		System.out.println("===========");
		System.out.println(count);

	} // end of main

} // end of class