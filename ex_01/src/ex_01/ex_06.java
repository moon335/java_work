package ex_01;

public class ex_06 {

	// 코드의 시작점은 main함수
	public static void main(String[] args) {
		
		int age;
		int count;
		
		age = 1;
		count = 100;
		
		System.out.println(age);
		System.out.println("================================");
		System.out.println(count);
		
		age = 500;
		count = 300;
		
		System.out.println(age);
		System.out.println("======================");
		System.out.println(count);
		
		int myAge = 800;
		System.out.println(myAge);
		System.out.println(9000);

	} // end of main

} // end of class
