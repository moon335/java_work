package ch01;

public class DataType03 {

	// 코드의 시작점은 main함수
	public static void main(String[] args) {

		// 실수형 2가지 
		// 1. float - 4byte 
		// 2. double - 8byte
		
		// 실수형의 기본 연산 단위가 double이다.(더 정확한 값을 나타낼 수 있기 때문)
		float a = 0.5F;
		float b = 12.9f; // 소문자 가능
		
		double c = 0.12345;
		double d = 0.55; // 8바이트
		
	} // end of main
	
} // end of class
